    /**
     * Determine merchant category type
     *
     * @param numericCode The 4-digit MCC code
     * @return Category type as string
     */
    public static String getMccCategoryType(Long numericCode) {
        int length = numericCode == 0 ? 1 : (int) Math.log10(Math.abs(numericCode)) + 1;
        if (length != 4) {
            throw new RuntimeException("Invalid MCC code " + numericCode);
        }

        if (numericCode >= 0 && numericCode <= 1499) {
            return "Agricultural Services";
        } else if (numericCode >= 1500 && numericCode <= 2999) {
            return "Contracted Services";
        } else if (numericCode >= 3000 && numericCode <= 3299) {
            return "Airlines";
        } else if (numericCode >= 3300 && numericCode <= 3499) {
            return "Car Rental";
        } else if (numericCode >= 3500 && numericCode <= 3999) {
            return "Lodging";
        } else if (numericCode >= 4000 && numericCode <= 4799) {
            return "Transportation Services";
        } else if (numericCode >= 4800 && numericCode <= 4999) {
            return "Utility Services";
        } else if (numericCode >= 5000 && numericCode <= 5599) {
            return "Retail Stores";
        } else if (numericCode >= 5600 && numericCode <= 5699) {
            return "Clothing Stores";
        } else if (numericCode >= 5700 && numericCode <= 7299) {
            return "Miscellaneous Stores";
        } else if (numericCode >= 7300 && numericCode <= 7999) {
            return "Business Services";
        } else if (numericCode >= 8000 && numericCode <= 8999) {
            return "Professional Services and Membership Organizations";
        } else if (numericCode >= 9000 && numericCode <= 9999) {
            return "Government Services";
        }

        return "Unknown Category";
    }

